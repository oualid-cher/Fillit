#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t		i;
	size_t		n;

	i = 0;
	if (s != 0 && f != 0)
	{
		n = ft_strlen(s);
		while (i < n)
			f(&s[i++]);
	}
}
